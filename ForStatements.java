public class ForStatements {
    public static void main(String[] args) {
        for(int i = 10; i > 0; i--){
            System.out.println("i = " + i);
            /**
             * i++ is i=i+1
             * for(initial value; condition; increment/decrement)
             */
        }
    }
}
